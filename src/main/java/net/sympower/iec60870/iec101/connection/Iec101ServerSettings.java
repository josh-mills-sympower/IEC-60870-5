package net.sympower.iec60870.iec101.connection;

import net.sympower.iec60870.common.IEC60870Settings;

public class Iec101ServerSettings extends IEC60870Settings {
    
    private String portName;
    private int baudRate = 9600;
    private int dataBits = 8;
    private int stopBits = 1;
    private int parity = 0;
    private int linkAddress = 1;

    public Iec101ServerSettings() {
        super();
    }

    public String getPortName() {
        return portName;
    }

    public void setPortName(String portName) {
        this.portName = portName;
    }

    public int getBaudRate() {
        return baudRate;
    }

    public void setBaudRate(int baudRate) {
        this.baudRate = baudRate;
    }

    public int getDataBits() {
        return dataBits;
    }

    public void setDataBits(int dataBits) {
        this.dataBits = dataBits;
    }

    public int getStopBits() {
        return stopBits;
    }

    public void setStopBits(int stopBits) {
        this.stopBits = stopBits;
    }

    public int getParity() {
        return parity;
    }

    public void setParity(int parity) {
        this.parity = parity;
    }

    public int getLinkAddress() {
        return linkAddress;
    }

    public void setLinkAddress(int linkAddress) {
        this.linkAddress = linkAddress;
    }

}
